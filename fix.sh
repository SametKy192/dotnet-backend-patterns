#!/bin/bash

i=$(git rev-list --count HEAD)

case $i in
23|22) d="2026-04-17 10:00:00" ;;
21|20|19) d="2026-04-18 12:00:00" ;;
18|17|16) d="2026-04-19 12:00:00" ;;
15|14|13) d="2026-04-20 12:00:00" ;;
12|11|10) d="2026-04-21 12:00:00" ;;
9|8) d="2026-04-22 12:00:00" ;;
7|6|5) d="2026-04-23 12:00:00" ;;
4|3) d="2026-04-24 12:00:00" ;;
2) d="2026-04-25 12:00:00" ;;
1) d="2026-05-03 12:00:00" ;;
esac

GIT_AUTHOR_DATE="$d" GIT_COMMITTER_DATE="$d" git commit --amend --no-edit
